<textarea name="textarea" class="transpa"> Estas bebidas se obtienen a través de la destilación. Un proceso que consiste en hervir jugos o zumos ya fermentados para separar el alcohol del agua y por lo tanto obtener una mayor graduación alcohólica. Entre lo 16º y los 45º. Aunque en algunas ocasiones se le añaden ingredientes para aumentar sus aromas y sabor como frutas. Dentro de estas bebidas se encuentran: Aguardientes, Brandy y Coñac a partir de jugo de uva, Whisky a partir de cebadas, ron a aprtir de caña de azúcar o tequilas a partir de agaves. Dentro de este grupo podemos diferenciar dos grandes partes:

<li><h2>Bebida alcohólica de agua y etanol. Elaboradas por mezcla directa de agua y alcohol etílico evitando los aromas del producto fermentado de origen. Este es el caso del vodka, obtenido normalmente de cocciones fermentadas de patata.</h2></li>

<li><h2>Bebidas de alcohol compuesto. En estos productos se utilizan productos ajenos al vino o aguardiente de partida que aportan sabor en la última fase de elaboración. Es el caso de la ginebra (con enebro), el pacharán (con endrinas) o absenta.</h2></li>


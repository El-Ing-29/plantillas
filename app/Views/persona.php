Cuando hablamos de bebidas alcohólicas debemos tener en cuenta que todas ellas tienen un rasgo común y es la presencia de alcohol etílico, también llamado etanol. El etanol es un compuesto de origen natural que se obtiene gracias a la fermentación de los azúcares presentes en cualquier compuesto. Aunque con esta característica en común son muy numerosas todas las bebidas alcohólicas que, a lo largo de la historia, se han elaborado. Todas ellas se pueden clasificar en referencia a distintas características, pero la más utilizada y la que emplearemos en esta ocasión es según su procedencia, o forma de elaboración.

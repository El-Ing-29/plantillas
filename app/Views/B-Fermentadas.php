<h1 class="text">Bebidas Fermentadas</h1>
<textarea name="textarea" class="transpa">
Estas son las primeras bebidas que existieron y se elaboraron, se denominan así debido al mecanismo llamado fermentación alcohólica, en el que el azúcar que se encuentra en las frutas, semillas o hierbas se convierte en alcohol debido a la acción de microorganismos como levaduras. estas bebidas tienen una  graduación alcohólica que oscila entre los 5º y los 15º, dependiendo de las concentraciones de azúcar de las materias primas con que se elaboren. Las bebidas principales fermentadas son:

<li><h2>Vino:</h2> El alcohol procede de la fermentación de los azúcares presentes en la uva, dentro de ellos entrarían los vinos blancos, tintos, rosados y espumosos.  El consumo de vino está asociado a una serie de beneficios para la salud, estudios y científicos de todo el mundo no dejan de descubrirnos todo lo bueno que conlleva el consumo de una copa de vino tinto diaria, frente a otro tipo de bebidas. Algunos de los beneficios de consumir vino tinto son: Retarda el envejecimiento,es un aliado para bajar peso, potenciador para nuestro cerebro, trata las infecciones de las encías o protege contra las quemaduras solares graves.</li>

<li><h2>Cerveza:</h2> El alcohol procede de la fermentación de los azúcares presentes en el caldo de cocción de cereales malteados.</li>

<li><h2>Sidra:</h2> El alcohol procede de la fermentación de los azúcares presentes en el jugo de la manzana.</li>

<li><h2>Hidromiel:</h2> El alcohol procede de la fermentación de los azúcares de la miel disuelta en agua. Hay productos similares en los que la miel se sustituye por otros azúcares complejos procedentes de siropes o incluso melazas.</li>
